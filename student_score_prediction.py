"""## 1.5 Load the Data, inspect data types and missing values"""

import pandas as pd
data = pd.read_csv('StudentsPerformance.csv')
print("Data types:")
print("---------------------------------------------------------")
print(data.dtypes)
print("---------------------------------------------------------")
print("\nMissing values:")
print("---------------------------------------------------------")
print(data.isnull().sum())
